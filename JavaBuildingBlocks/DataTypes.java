/**
 * DataTypes
 */
public class DataTypes {

    public static void main(String[] args) {

        // Basic primitive data types
        int i = 1234567890;         // Integer type -   4 bytes	Stores whole numbers from -2,147,483,648 to 2,147,483,647
        float f = 5.5f;             // Floating point or Decimal point numbers
        char c = 'a';               // Characte
        boolean b = true;           // Boolean

        System.out.println(i);
        System.out.println(f);
        System.out.println(c);
        System.out.println(b);
        
        // Big primitive data types
        long li = 9223372036854775807L;


    }
}